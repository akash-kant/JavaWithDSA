public class typepromotion {
     public static void main(String args[]){
        char a = 'a';
        char b ='b';
        System.out.println((int)(b));
        System.out.println((int)(a));
        System.out.println(a); // type promotions are done only with the expressions like +,-,*,/. 
        System.out.println(b-a);//with the type promotion the values are found.
     }
}
